// Arithmetic operators
let additionResult = 10 + 5; // Addition
let subtractionResult = 10 - 5; // Subtraction
let multiplicationResult = 10 * 5; // Multiplication
let divisionResult = 10 / 5; // Division
let remainderResult = 10 % 3; // Finding remainder

console.log(`Addition: ${additionResult}`);
console.log(`Subtraction: ${subtractionResult}`);
console.log(`Multiplication: ${multiplicationResult}`);
console.log(`Division: ${divisionResult}`);
console.log(`Remainder: ${remainderResult}`);

// Concatenation with the addition operator
let string1 = "Hello";
let string2 = " World";
let concatenationResult = string1 + string2;
console.log(`Concatenation: ${concatenationResult}`);

// Boolean values and arithmetic operators
let trueResult = true + 5; // true is converted to 1
let falseResult = false - 3; // false is converted to 0

console.log(`True Result: ${trueResult}`);
console.log(`False Result: ${falseResult}`);

// Shorthand operator +=
let number = 10;
number += 5; // equivalent to number = number + 5
console.log(`Shorthand +=: ${number}`);

// Increment and Decrement operators
let num = 5;
let preIncrement = ++num; // Increment before assignment
let postIncrement = num++; // Increment after assignment

console.log(`Pre-increment: ${preIncrement}`);
console.log(`Post-increment: ${postIncrement}`);
console.log(`Updated num: ${num}`); // num is now 7 after post-increment

// Exponentiation operator (**)
let base = 2;
let exponent = 3;
let cubeResult = base ** exponent; // 2^3 = 8

console.log(`Cube using exponentiation: ${cubeResult}`);

// Finding squares using exponentiation
let squareResult = base ** 2; // 2^2 = 4

console.log(`Square using exponentiation: ${squareResult}`);
