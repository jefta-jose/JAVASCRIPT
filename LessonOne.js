// Declare a variable using the "let" keyword
let myNumber;

// Assign a value to the variable
myNumber = 42;

// Log the value to the console
console.log("The value of myNumber is:", myNumber);

// Declare and assign a variable in one line
let myString = 'Hello, World!';

// Log the string to the console
console.log("The value of myString is:", myString);

// Change the value of a variable
myNumber = 100;

// Log the updated value to the console
console.log("The updated value of myNumber is:", myNumber);

// Avoid declaring a variable multiple times
let myVariable = 'Initial Value';
console.log("Original value of myVariable:", myVariable);

// Trying to redeclare the variable will result in an error
// let myVariable = 'New Value'; // Uncommenting this line will cause an error

// Best practices for variable naming
let userName = 'JohnDoe';
let itemCount = 5;

// Summary
console.log("Summary:");
console.log("Variable userName:", userName);
console.log("Variable itemCount:", itemCount);
